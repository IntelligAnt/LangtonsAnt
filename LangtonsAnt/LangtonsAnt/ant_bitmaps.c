const unsigned char* get_ant_bitmap(size_t size, Direction dir)
{
	const unsigned char *ant24_bitmap[4];
	const unsigned char *ant8_bitmap[4];

	ant24_bitmap[DIR_UP]    = ant24_up;
	ant24_bitmap[DIR_RIGHT] = ant24_rt;
	ant24_bitmap[DIR_DOWN]  = ant24_dn;
	ant24_bitmap[DIR_LEFT]  = ant24_lf;

	ant8_bitmap[DIR_UP]     = ant8_up;
	ant8_bitmap[DIR_RIGHT]  = ant8_rt;
	ant8_bitmap[DIR_DOWN]   = ant8_dn;
	ant8_bitmap[DIR_LEFT]   = ant8_lf;

	switch (size) {
	case 8:
		return ant8_bitmap[dir];
	case 24:
		return ant24_bitmap[dir];
	default:
		return NULL;
	}
}
